#include <lnote.h>

void main()
{
    HRESULT hr = LNote_InitAudio();
    if ( FAILED( hr ) ) return;

    LNHandle sound = LSound_Load( "../../Media/ln28ti.ogg" );
    if ( sound )
    {
        LSound_Play( sound );
        getchar();
    }

    LNote_End();
}

