//=============================================================================
class LGraphics
    : public LSingletonRefClass< LGraphics, Core::Graphics::Manager >
{
public:

    /// インスタンスの取得
    static LGraphics getInstance();

public:

    //---------------------------------------------------------------------
	///**
    //  @brief      ゲームスクリーンのサイズを取得する
    //
    //  @par
    //              ウィンドウモード、フルスクリーンモードにかかわらず、
    //              現在のスクリーンサイズをピクセル単位で返します。<br>
    //              (フルスクリーンモードで画面が拡大されている場合でも
    //               常にウィンドウモードのときと同じサイズを返します)
    //*/
    //---------------------------------------------------------------------
    LVector2 getScreenSize() const;

    //---------------------------------------------------------------------
	///**
    //  @brief      背景色を設定する
    //
    //  @param[in]  color_ : 背景色として設定する色
    //
    //  @par
    //              アルファ値は無視されます。
    //*/
    //---------------------------------------------------------------------
	void setBGColor( const LColor& color_ );

    //---------------------------------------------------------------------
	///**
    //  @brief      背景色を設定する
    //
    //  @param[in]  r_ : R (赤) 成分 (0.0 〜 1.0)
    //  @param[in]  g_ : G (緑) 成分 (0.0 〜 1.0)
    //  @param[in]  b_ : B (青) 成分 (0.0 〜 1.0)
    //*/
    //---------------------------------------------------------------------
	void setBGColor( lnfloat r_, lnfloat g_,lnfloat b_ );



};

