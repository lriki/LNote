    RClass* Class_LNException::mRubyClass_LNExitException = NULL;

    //---------------------------------------------------------------------
    //
    //---------------------------------------------------------------------
    void Class_LNException::registerRubyObject()
    {
        mRubyClass_LNExitException = mrb_define_class( Engine::getMRBState(), "LNExitException", Engine::getMRBState()->eException_class );
    }

    //---------------------------------------------------------------------
    //
    //---------------------------------------------------------------------
    void Class_LNException::raiseExit()
    {
        mrb_raise( Engine::getMRBState(), mRubyClass_LNExitException, "engine exit" );
        //mrb_exc_raise( 
    }

    //---------------------------------------------------------------------
    //
    //---------------------------------------------------------------------
    void Class_LNException::raiseArgumentError_wrong_number_of_arguments()
    {
        mrb_state* mrb = Engine::getMRBState();
        mrb_raise( Engine::getMRBState(), E_ARGUMENT_ERROR, "wrong number of arguments" );
    }
 


