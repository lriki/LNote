    /// キーフレームソート用比較関数
    static bool scalarCompareFunc( const AnimationKey& l_, const AnimationKey& r_ )
    {
	    return l_.FramePos < r_.FramePos;
    }

    //---------------------------------------------------------------------
	//
    //---------------------------------------------------------------------
    void EffectMasterAnimationTrack::addKey( const AnimationKey& key_ )
    {
        mAnimationKeyArray.push_back( key_ );

        std::sort( mAnimationKeyArray.begin(), mAnimationKeyArray.end(), scalarCompareFunc );
    }

