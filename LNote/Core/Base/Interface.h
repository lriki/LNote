//=============================================================================
// ■ Interface クラス
//-----------------------------------------------------------------------------
///**
//  @brief      参照カウントを持つオブジェクトのインターフェース            
//*/
//=============================================================================
class Interface
{
public:

	/// 参照カウントの取得
	virtual s32 getRefCount() = 0;

	/// 参照カウントをひとつ増やす
	virtual s32 addRef() = 0;

	/// 参照カウントをひとつ減らす
	virtual s32 release() = 0;

    /// SAFE_RELEASE() マクロ対応版 release()
    inline s32 Release() { return release(); }

protected:

	virtual ~Interface() {}
};

